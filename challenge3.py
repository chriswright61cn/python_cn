# Check if the variable is divisible by 3 or 5. If it is print
# “This number is divisible by 3 or 5”. Otherwise print
# “This number is not divisible by 3 or 5”.

num = 7
if num % 3 == 0 or num % 5 == 0:
    print('divisible by three or five')
else:
    print('not divisible by three or five')

